            <b>NEUMOSUR ADVIERTE QUE NADAR EN PISCINAS CON EXCESO DE CLORO PUEDE AUMENTAR LAS POSIBILIDADES DE QUE UN NIÑO DESARROLLE SÍNTOMAS ASMÁTICOS. </b> 
