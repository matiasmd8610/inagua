               Durante el verano, resulta habitual el uso de las piletas de natación para combatir el calor y como modo de esparcimiento. Sin embargo, para evitar problemas para la salud, es necesario tener en cuenta algunas recomendaciones necesarias para el buen mantenimiento del agua de las piscinas.
                 
          
<br><br>
              A través del agua pueden transmitirse enfermedades como otitis, conjuntivitis y diversas infecciones intestinales. El mantenimiento del agua en condiciones saludables ayuda a prevenir esos inconvenientes, y para ello es necesario utilizar una serie de productos para evitar por ejemplo la presencia de bacterias y algas.
