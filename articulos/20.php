        El Dengue es una enfermedad viral transmitida por la picadura del mosquito Aedes Aegypti que tiene una
        incubación de 3 a 8 días y se presenta en 2 formas: Dengue y Dengue hemorrágico.
        <br>
        El primero de estos, el Dengue clásico, tiene apariciones bruscas de fiebre alta, dolor detrás de los ojos,
        dolor de cabeza y en los músculos y articulaciones (como un cuadro gripal), con náuseas y vómitos.
        <br>
        El segundo caso, el Dengue hemorrágico, tiene los mismos síntomas que el clásico pero con severos y continuos
        dolores de estómago, sangrado en diferentes partes del cuerpo y vómitos frecuentes con o sin sangre.
        <br>
        Este virus se propaga rápidamente con el agravante de que se presenta de manera epidémica, ataca todos por igual
        ya que ninguna persona tiene defensas para contrarrestarlo, produce un gran decaimiento, su recuperación es
        lenta y no hay medicamentos para combatirla.
        <br>
        La enfermedad se desarrolla en zonas densamente pobladas, especialmente las que tienen climas tropicales y
        subtropicales y la región afectada pasa a convertirse en riesgosa.
        <br><br>
