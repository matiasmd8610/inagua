        <b>¿Por qué es importante el pH? </b>
        <br>

        "EL VALOR IDEAL DE pH EN LA PISCINA DEBE ESTAR SITUADO ENTRE 7,2 Y 7,6". Anteriormente se indica que para la
        destrucción de bacterias, debemos mantener en el agua una cantidad de cloro residual suficiente para ello; pero
        este cloro sólo actuará eficazmente como bactericida cuando el agua donde se diluye tenga un pH comprendido
        entre 7,2 y 7,6. Por lo indicado, es básico que si se tienen que destruir las bacterias se debe de mantener a
        toda costa el pH entre los valores mencionados.
        <br><br>
        Existen otros factores que obligan a mantener el pH correcto. De ser superior, a 7,6 el ácido disuelto en el
        agua se precipitará de forma visible, enturbiando el agua y dándole un aspecto lechoso, obstruyendo rápidamente
        el filtro y precipitándose en las paredes y accesorios de la piscina. Cuando el valor de pH sea inferior a 7,2
        el agua será corrosiva, produciendo irritaciones en los ojos y mucosas nasales, pudiendo llegar a destruir las
        partes metálicas de la instalación de filtración.


