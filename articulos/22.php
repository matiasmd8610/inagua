        Sevilla, 14 de julio de 2009.- Coincidiendo con la llegada de las vacaciones y el comienzo de la temporada de
        piscinas, la Asociación de Neumólogos del Sur (Neumosur) advierte a los padres que nadar en piscinas con exceso
        de cloro puede aumentar las posibilidades de que un niño desarrolle síntomas de asma, y que esta probabilidad
        aumenta cuanto más pequeño sea el niño, más tiempo permanezca en el agua, más agua ingieran los niños, menos
        higiene personal se requiera a la hora de zambullirse, y mayor sea la temperatura del agua.
        <br><br>

        El riesgo es mayor en los niños que acuden regularmente por debajo de los 6-7 años de edad, porque a esa edad no
        se nada, sino que se chapotea y se inhalan y degluten más aerosoles y gotitas con cloramina, que es la sustancia
        que puede lesionar el epitelio pulmonar y provocar síntomas asmáticos (tos, pitos en el pecho y ahogo) o
        desencadenar asma en niños predispuestos (atópicos). Esta sustancia se genera con la mezcla entre el ácido
        hipocloroso –desinfectante resultante de la adición al agua del cloro- y el sudor, la saliva y la orina del agua
        de la piscina -bastante frecuente ésta última cuando es de niños pequeños.
        <br><br>
        Neumosur subraya que los niveles de cloramina son mayores en las piscinas con exceso de cloro, no ventiladas
        (cubiertas), con mayor temperatura del agua y con malas condiciones higiénicas de los nadadores, por lo que
        aconseja a los padres que se cercionen de que las instalaciones a las que llevan a sus hijos tienen los permisos
        correspondientes y eviten en cualquier caso aquellas cuyas condiciones les resulten sospechosas. Igualmente, les
        recomienda que no dejen a sus hijos un tiempo demasiado prolongado en el agua, que estén atentos de que no
        traguen agua, y que exijan a los padres de niños pequeños el uso de pañales adecuados capaces de absorber la
        orina. Asimismo, les recuerda que los pulmones de los niños, cuantos más pequeños son, más vulnerables resultan
        a los irritantes como el cloro, y recomienda especial precaución con los lactantes.

        <br><br>
        El asma afecta a más de 300 millones de personas en el mundo y es la causa más frecuente de enfermedad infantil
        crónica. Neumosur recuerda a los padres de hijos asmáticos que sus hijos pueden, y deben, hacer deporte pero que
        deben seguir una serie de normas y consultar con su médico (neumólogo, alergólogo, pediatra o médico de atención
        primaria) cualquier duda que se plante en al respecto. El asmático puede hacer cualquier deporte pero hay una
        serie de ellos más indicados que otros. La natación puede ser un deporte adecuado para los asmáticos pero no se
        debe olvidar que los bronquios de estos pacientes “pueden responder de forma exagerada a estímulos externos”. De
        esta forma, sustancias irritantes como el cloro pueden hacer que sus bronquios “se cierren” y provoquen los
        síntomas asmáticos o desencadenar un asma “latente” en sujetos predispuestos (atópicos). De igual forma, puede
        ocurrir este hecho ante cambios bruscos de temperatura como tirarse bruscamente en un agua muy fría.
        <br><br>
        Además de los niños menores de 6-7 años, el exceso de cloro en las piscinas lo acusan antes los sujetos con
        alergia o enfermedad respiratoria, así como las personas más expuestas, como monitores, cuidadores, personal de
        mantenimiento..., a las que recomienda ser inflexibles y exigentes en el cumplimiento de las adecuadas
        condiciones higiénicas en la piscina y su entorno.
