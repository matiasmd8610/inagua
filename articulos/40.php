           Todos estos productos son regulados por la ANMAT. Al momento de comprarlos debemos recordar que en su rótulo deberá constar el número de registro nacional de establecimiento (RNE) y el número de registro nacional de producto de uso doméstico (RNPUD). Para confirmar la legitimidad del producto es importante consultar el listado de productos para tratamiento de aguas de piscinas. 
<br><br>

          A fin de asegurar la efectividad de los productos para piscinas, es muy importante respetar estrictamente las indicaciones del rótulo, en lo que se refiere a dosificación, precauciones y condiciones de almacenamiento. Además, siempre deben mantenerse fuera del alcance de los niños y de las mascotas.
<br><br>
          <b>Cloro</b>  

<br>
          El cloro mantiene el agua libre de bacterias, ya que su función es destruir estos microorganismos. Por eso, es importante realizar el tratamiento con la cantidad de cloro adecuada, teniendo en cuenta el volumen de agua que contenga la pileta. 
 <br>
          Después de haber dosificado el cloro, debe controlarse periódicamente que su nivel no sea excesivamente alto o bajo, y para ello pueden utilizarse kits de tiras de prueba que permiten su medición. 
 <br>
          El nivel de cloro debe mantenerse en 2 partes por millón (ppm). No es aconsejable que se encuentre en un valor mayor y, por otra parte, los bajos niveles de cloro pueden permitir el desarrollo de bacterias. 

 
<br><br>
 <b>PH del agua   </b>  

<br>
          En las piletas de natación es importante controlar el pH del agua al menos una vez por semana, utilizando el kit de medición, pues ello influye directamente en la efectividad del cloro.  
<br>
         El pH debe encontrarse en un valor de 7,2 a 7,6. Si el pH es mayor o menor, la efectividad del cloro como germicida disminuye.
<br>
          Por lo tanto, en caso de que el pH no se encuentre en los niveles adecuados, debe utilizarse un producto incrementador (alcalinizante) o reductor (acidificante) del mismo.  
<br><br>
 <b>Alguicidas </b>  

<br>
          También puede requerirse la utilización de alguicidas para eliminar las algas presentes en la piscina, las cuales provocan que el agua se torne verdosa y que las paredes y el fondo de la pileta se vuelvan resbaladizos.  
<br>
          Por eso, resulta necesario añadir periódicamente la dosis adecuada de alguicida, sobre todo cada vez que se renueve el agua de la piscina.   
<br><br>
         <b>Floculantes  </b>  

<br>
         Estas sustancias son utilizadas en el mantenimiento de piscinas en las que el agua se ha vuelto turbia debido a la presencia de partículas en suspensión. Los floculantes aumentan el tamaño de dichas partículas y provocan su decantación al fondo de la pileta, facilitando su limpieza mediante la utilización del barrefondos.
