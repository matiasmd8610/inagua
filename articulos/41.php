      <p class="texto31 ">
        En primer lugar, debemos tener en cuenta que la superficie debe estar libre de óxido, grasa, polvillo, pintura
        en mal estado (suelta, ampollada, descascarada, etc), alcalinidad, etc.


        <br><br>
        <b> Atención:</b><i> si bien la preparación de superficie es importante en todos los trabajos de pintura, en el
          caso de piletas de natación es fundamental ya que las superficies están sometidas a condiciones extremas y el
          éxito del esquema depende totalmente de la calidad de la preparación del sustrato.</i>
        <br><br>

        <b>Preparación </b>
        <br>
        Superficies nuevas (mampostería, revoques y hormigón)
        En piletas nuevas con alcalinidad, dejar transcurrir 6 meses o realizar el tratamiento ácido. En superficies muy
        lisas, realizar el mismo tratamiento. Es indispensable que la superficie esté exenta de alcalinidad.
        <br><br>
        <b>Superficies previamente pintadas</b>
        <br>
        Si la pintura existente fuese a la cal, debemos desprenderla utilizando cepillo de cerdas duras, lija gruesa o
        arenado. Luego, limpiar a fondo la superficie con cepillo, agua y detergente para eliminar los restos de
        grasitud y suciedad. Enjuagar con abundante agua. Si se debiera reparar algún defecto superficial, hacerlo con
        un mortero de cemento, realizando posteriormente el tratamiento ácido en esas áreas y continuar el esquema como
        si fuera una superficie nueva. En caso de pinturas de base acuosa debemos eliminar por completo la película
        existente dado que existen altas posibilidades de que sea removida por el recubrimiento, generando problemas de
        ampollamiento. Luego tratar como si fuera superficie nueva. Si la pintura es de caucho clorado lijamos con lija
        gruesa o cepillo de acero para generar mordiente para las posteriores manos de pintura y también para eliminar
        pintura suelta, descascarada, ampollada, etc.
        <br>
        <b>CONSEJO: </b>Es conveniente colocar hilera de azulejos a la altura de la línea de flotación para facilitar la
        posterior limpieza de tu pileta de natación.
        <br><br>


        <b>Aplicacion </b>

        <br>
        Una vez preparada la superficie, desprender con un cepillo de acero toda la arena que hubiera podido quedar
        adherida. La primer mano la aplicamos con pincel. La segunda mano la podemos realizar a rodillo. Entre manos
        debemos dejar un lapso de 12 horas y 7 días en verano o 14 en invierno, en condiciones normales de temperatura y
        humedad, antes de llenar la pileta con agua. Si las condiciones del clima no son buenas se debe dejar secar
        durante un período de tiempo mayor. Evitar pintar bajo los rayos directos del sol y/o cuando la superficie se
        encuentre con alta temperatura, ya que se perjudica la adherencia por la excesiva velocidad de evaporación del
        solvente.

        <br><br>
        <b>Tratamiento con ácido muriático</b>

        <br>
        Esta recomendado para proveer mayor anclaje y adherencia a la pintura como así también a neutralizar las
        superficies nuevas que van a ser pintadas. Limpiar a fondo la superficie con cepillo de cerdas, agua y
        detergente, para eliminar los restos de grasitud, suciedad, etc. Enjuagar con abundante agua. Esta operación es
        muy importante para que el sustrato se encuentre apto para comenzar el tratamiento. Aplicar entonces, ácido
        muriático diluido en partes iguales con agua (A) y dejar actuar durante 10 minutos. Si el grado de burbujeo
        fuese muy intenso (B), aplicar 2 veces ácido en el mismo sitio con un intervalo de 30 segundos y luego dejarlo
        actuar 10 minutos. Una vez transcurridos los 10 minutos enjuagar con abundante agua (C) y finalmente, dejar
        secar un mínimo de 48 horas. Una vez realizado el tratamiento, es indispensable verificar que la superficie se
        encuentre libre de alcalinidad. Para ello se debe utilizar un indicador o papel pH. Si indica un valor superior
        a 7-8, se deberá repetir el tratamiento ácido tantas veces como sea necesario hasta alcanzar ese valor. Si el
        mismo fuera menor a 7-8, debe enjuagarse a fondo la superficie con mucha agua. En superficies muy lisas,
        realizar tratamiento ácido.
        <br><br>
        <b> IMPORTANTE:</b> El nivel óptimo de adherencia se logrará cuando la superficie quede con una rugosidad
        similar a la de una lija de grano 120.

